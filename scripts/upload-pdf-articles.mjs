async function uploadAndCreate() {
  for (const art of articles) {
    const filePath = resolve('assets/pdfs', art.file)
    console.log(`\n📄 Processing: ${art.file}`)

    // 1. Upload PDF to Sanity CDN
    const fileBuffer = readFileSync(filePath)
    const asset = await client.assets.upload('file', fileBuffer, {
      filename: art.file,
      contentType: 'application/pdf',
    })
    console.log(`  ✅ Uploaded: ${asset._id} (${(asset.size / 1024).toFixed(0)} KB)`)

    // 2. Build Portable Text body
    const bodyBlocks = art.body.map((para, i) => ({
      _type: 'block',
      _key: `block-${i}`,
      style: 'normal',
      markDefs: [],
      children: [{ _type: 'span', _key: `span-${i}`, text: para, marks: [] }],
    }))

    // 3. Create article document
    const doc = await client.create({
      _type: 'article',
      title: art.title,
      slug: { _type: 'slug', current: art.slug },
      excerpt: art.excerpt,
      publishedAt: `${TODAY}T08:00:00.000Z`,
      contentType: art.contentType,
      difficulty: art.difficulty,
      tldr: art.tldr,
      body: bodyBlocks,
      downloadableFile: {
        _type: 'file',
        asset: { _type: 'reference', _ref: asset._id },
      },
      author: { _type: 'reference', _ref: AUTHOR_REF },
      category: { _type: 'reference', _ref: art.categoryRef },
    })
    console.log(`  ✅ Created: ${doc._id} — ${art.title}`)
  }

  console.log('\n🎉 All 15 articles created!')
}

uploadAndCreate().catch((err) => {
  console.error('❌ Error:', err)
  process.exit(1)
})
